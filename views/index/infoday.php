
			<h3 class="text-center text-primary">Проведение информационного дня Дорпрофсожа 17.09.2015 на Гомельском узле </h3>
			<p>
				&nbsp;&nbsp;&nbsp;Визит руководителей и специалистов Республиканского комитета профсоюза железнодорожников и транспортных строителей на Гомельский железнодорожный узел Гомельского отделения Белорусской железной дороги 17.09.2015 года был связан с проведением информационного дня Дорпрофсожа.
				<br>&nbsp;&nbsp;&nbsp;В рамках информационного дня председателем Дорпрофсожа Рингом В.И., его заместителем Вайтеховичем И.А. и главным техническим инспектором Новиком А.И. посещались Локомотивное депо Гомель, Гомельская дистанция пути  и Гомельский вагонный участок.
				<br>&nbsp;&nbsp;&nbsp;На предприятиях представители Дорпрофсожа посетили профсоюзные комитеты, провели встречи с руководителями организаций, беседовали с работниками.
				<br>&nbsp;&nbsp;&nbsp;В завершении своего пребывания на Гомельском железнодорожном узле руководством Дорпрофсожа было проведено совещание с профактивом Гомельского Райпрофсожа.
				<br>&nbsp;&nbsp;&nbsp;С положительной стороны была отмечена работа профсоюзных комитетов, их информационная деятельность, проводимые мероприятия по избирательной компании в Республике Беларусь, проведение детского летнего оздоровительного сезона и многое другое.
			</p>
